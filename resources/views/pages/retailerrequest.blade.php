                                    .attr("value", "")
                                    .text('Select City'));
                            for (let i = 0; i < data['success'].states.length; i++) {
                                $('#state_top')
                                    .append($("<option></option>")
                                        .attr("value", data['success'].states[i].id)
                                        .text(data['success'].states[i].name));
                            }
                        }
                    }
                });
            }
        });

