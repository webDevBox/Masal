                            $('#city')
                                .find('option')
                                .remove()
                                .end();
                            if (data['success'] !== undefined) {
                                $('#city')
                                    .append($("<option></option>")
                                        .attr("value", "")
                                        .text('Select City'));
                                for (let i = 0; i < data['success'].cities.length; i++) {
                                    $('#city')
                                        .append($("<option></option>")
                                            .attr("value", data['success'].cities[i].id)
                                            .text(data['success'].cities[i].city));
                                }
                            }
                        }
                    });
                }
            });
        });

